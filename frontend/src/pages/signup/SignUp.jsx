import { Link } from "react-router-dom";
import GenderCheckbox from "./GenderCheckbox";
import { useState } from "react";
import useSignup from "../../hooks/useSignup";
import logo from '../login/mylogo.png';

const SignUp = () => {
	const [inputs, setInputs] = useState({
		fullName: "",
		username: "",
		password: "",
		confirmPassword: "",
		gender: "",
	});

	const { loading, signup } = useSignup();


	const handleCheckboxChange = (gender) => {
		setInputs({ ...inputs, gender });
	};

	const handleSubmit = async (e) => {
		e.preventDefault();
		await signup(inputs);
	};

	return (
		<div className='flex flex-col items-center justify-center min-w-96 mx-auto backdrop-blur-lg'>
			<h1 className='text-5xl font-semibold text-center text-gray-300 mb-10 select-none'>
				<img src={logo} alt="Queued" className="w-50 h-40 opacity-80" />
				<span className='text-neutral-800'> Queued</span>
			</h1>
			<div className='w-full p-6 rounded-lg shadow-md bg-gray-400 bg-clip-padding backdrop-filter backdrop-blur-lg bg-opacity-0'>
				

				<form onSubmit={handleSubmit}>
					<div className="flex flex-col gap-3">
						<input
							type='text'
							placeholder='Full Name'
							className='w-full input input-bordered  h-10'
							value={inputs.fullName}
							onChange={(e) => setInputs({ ...inputs, fullName: e.target.value })}
						/>
						<input
							type='text'
							placeholder='Username'
							className='w-full input input-bordered h-10'
							value={inputs.username}
							onChange={(e) => setInputs({ ...inputs, username: e.target.value })}
						/>
						<input
							type='password'
							placeholder='Password'
							className='w-full input input-bordered h-10'
							value={inputs.password}
							onChange={(e) => setInputs({ ...inputs, password: e.target.value })}
						/>
						<input
							type='password'
							placeholder='Confirm Password'
							className='w-full input input-bordered h-10'
							value={inputs.confirmPassword}
							onChange={(e) => setInputs({ ...inputs, confirmPassword: e.target.value })}
						/>

						<GenderCheckbox onCheckboxChange={handleCheckboxChange} selectedGender={inputs.gender} />
					</div>

					<div className="mt-5">
						<button className='btn btn-block btn-sm mt-2 text-lg' disabled={loading}>
							{loading ? <span className='loading loading-spinner '></span> : "Signup"}
						</button>
					</div>

					<div className="flex gap-2 justify-center items-center mt-5">
						<span>
							Have an account?
						</span>
						<Link to='/login' className='text-md hover:underline hover:text-blue-600 inline-block'>
							Login
						</Link>
					</div>
				</form>
			</div>
		</div>
	);
};
export default SignUp;

